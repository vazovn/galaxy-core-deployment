<p>{cfg:site_name} allows you to upload big files (up to 100 GB) into your Galaxy-based portal. </p>

<h2><font color="red"><b>HOW TO UPLOAD</b></font></h2>
<p>
    
    
    Log into Filesender and select and upload your files.
    </br></br>
    Go back to the Portal and import them into the Portal using the tools menu item 
    </br></br>
    "Import files" > "Upload File"
    </br></br>Please select the "Choose FTP file" button to view the files uploaded by Filesender and import them to the Portal history.
    
<h2><font color="red"><b>HOW TO REMOVE</b></font></h2>    
<p>
    
    
    To remove the files uploaded into Filesender, just go to "My Transfers" in the Filesender menu and click on the garbage icon.
    </br>
    </br>
    Removing the files from Filesender does not remove them from the Portal!!
    
</p>    
    
